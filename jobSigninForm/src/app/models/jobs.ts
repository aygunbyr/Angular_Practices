export class Jobs {
    name: String;
    email: String;
    work_exp: String;
    cover_letter: String;
    gender: String;

    constructor(name: String, email: String, work_exp: String, cover_letter: String, gender: String) {
        this.name = name;
        this.email = email;
        this.work_exp = work_exp;
        this.cover_letter = cover_letter;
        this.gender = gender;
    }

}

